    public void iniciaControle(){
        areaChat.setText(areaChat.getText()+"> Cliente Iniciado.\n");
        try {
            nomeLabel2.setText("");
            cChat = new ClientChat(areaChat,buttonSend,nomeLabel2);
            cChat.cliente = new Client(nomeCliente);
            
            servidor = (IServer) Naming.lookup("rmi://localhost:56787/ChatService");
            areaChat.setText(areaChat.getText()+"> Servidor Conectado.\n");
            areaChat.setText(areaChat.getText()+"> Aguardando Atendente.\n");
            conexao = (IChat)servidor.requestAtendent(cChat);
            
            nomeLabel1.setText(nomeCliente+" "+cChat.cliente.getId());
            if(cChat.getConectado())
                nomeLabel2.setText(cChat.conexao.getName()+" "+cChat.conexao.getId());
            
            if(conexao != null){
                cChat.setConexao(conexao);
                conectou = true;
                conexao.setConexao(cChat);
                System.out.println("> Chamou [requestAtendent]");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


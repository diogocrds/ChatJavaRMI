            // TODO add your handling code here:
            IMessage msg = new Message();
            msg.setMessage(areaInput.getText());
            Calendar c = Calendar.getInstance();
            msg.setDate(c);
            SimpleDateFormat ft = new SimpleDateFormat ("dd.MM.yyyy '/' HH:mm:ss");
            areaChat.setText(areaChat.getText()+" "+ft.format(c.getTime())+" | "+nomeCliente+" > "+areaInput.getText()+"\n");
            cChat.conexao.deliver(msg);
            areaInput.setText("");

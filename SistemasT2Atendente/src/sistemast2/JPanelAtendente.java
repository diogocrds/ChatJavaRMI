    private void buttonEncerrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEncerrarActionPerformed
        //this.setVisible(false);
        
        buttonSend.setEnabled(false);
        buttonEncerrar.setEnabled(false);
        areaChat.setText(areaChat.getText()+"===> Atendimento Encerrado <===\n");
        areaChat.setText(areaChat.getText()+"> Aguardando novo cliente\n");
        try {
            aChat.conexao.closeConexao();
            servidor.freeAtendent(aChat);
        } catch (RemoteException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_buttonEncerrarActionPerformed

    private void buttonSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSendActionPerformed
        try {
            IMessage msg = new Message();
            msg.setMessage(areaInput.getText());
            Calendar c = Calendar.getInstance();
            msg.setDate(c);
            SimpleDateFormat ft = new SimpleDateFormat ("dd.MM.yyyy '/' HH:mm:ss");
            areaChat.setText(areaChat.getText()+" "+ft.format(c.getTime())+" | "+nomeAtedente+" > "+areaInput.getText()+"\n");
            aChat.conexao.deliver(msg);
            areaInput.setText("");
        } catch (RemoteException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_buttonSendActionPerformed

    private void closeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_closeActionPerformed
        try {
            servidor.requestLeave(aChat);
        } catch (RemoteException ex) {
            Logger.getLogger(JPanelAtendente.class.getName()).log(Level.SEVERE, null, ex);
        }
        System.exit(0);
    }//GEN-LAST:event_closeActionPerformed



    public void iniciaControle(){
        areaChat.setText(areaChat.getText()+"> Atendente Iniciado.\n");
        try {
            nomeLabel2.setText("");
            aChat = new AtendentChat(areaChat,conexao,buttonSend,buttonEncerrar,nomeLabel2);
            aChat.atendente = new Atendent(nomeAtedente);
            
            servidor = (IServer) Naming.lookup("rmi://localhost:56787/ChatService");
            areaChat.setText(areaChat.getText()+"> Servidor Conectado.\n");
            buttonSend.setEnabled(false);
            buttonEncerrar.setEnabled(false);
            areaChat.setText(areaChat.getText()+"> Aguardando Cliente.\n");
            servidor.requestJoin(aChat);

            nomeLabel1.setText(nomeAtedente+" "+aChat.atendente.getId());
            if(aChat.getConectado())
                nomeLabel2.setText(aChat.conexao.getName()+" "+aChat.conexao.getId());
            conectou = true;
            System.out.println("> Chamou [requestAtendent]");
        } catch (RemoteException | NotBoundException | MalformedURLException e) {
            e.printStackTrace();
            System.out.println("SISTEMA FORA DO AR");
            //this.setVisible(false);
            
        }
    }

